package model;

public class Body {

    private Armor hands;
    private Armor arms;
    private Armor chest;
    private Armor legs;
    private Armor feet;

    public Armor getHands() {
        return hands;
    }

    public void setHands(Armor hands) {
        this.hands = hands;
    }

    public Armor getArms() {
        return arms;
    }

    public void setArms(Armor arms) {
        this.arms = arms;
    }

    public Armor getChest() {
        return chest;
    }

    public void setChest(Armor chest) {
        this.chest = chest;
    }

    public Armor getLegs() {
        return legs;
    }

    public void setLegs(Armor legs) {
        this.legs = legs;
    }

    public Armor getFeet() {
        return feet;
    }

    public void setFeet(Armor feet) {
        this.feet = feet;
    }
}
